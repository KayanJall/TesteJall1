    public void LoadTableCon() {
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"idcontato", "Nome", "sobrenome", "telefone1", "telefone2"}, 0);
        for (int i = 0; i < ListaCon.size(); i++) {
            modelo.addRow(new Object[]{ListaCon.get(i).getIdcontato(), ListaCon.get(i).getNomeContato(), ListaCon.get(i).getSobrenome(), ListaCon.get(i).getTelefone1(), ListaCon.get(i).getTelefone2()});
        }
        tbl_con_cons.setModel(modelo);
    }

