    private void tbl_con_consMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_con_consMouseClicked
        int index = tbl_con_cons.getSelectedRow();
        if (index >= 0 && index < ListaCon.size());
        Contato C = ListaCon.get(index);
        c_con_idcontato.setText(String.valueOf(C.getIdcontato()));
        c_con_nome.setText(C.getNomeContato());
        c_con_sobrenome.setText(C.getSobrenome());
        c_con_telefone1.setText(C.getTelefone1());
        c_con_telefone2.setText(C.getTelefone2());

        modo = "click";
        Interface();
    }//GEN-LAST:event_tbl_con_consMouseClicked

