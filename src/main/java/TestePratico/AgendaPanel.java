    private void btn_con_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_con_salvarActionPerformed
        if (modo.equals("novo")) {
            int id = Integer.parseInt(c_con_idcontato.getText());
            Contato C = new Contato(id, c_con_nome.getText(), c_con_sobrenome.getText(), c_con_telefone1.getText(), c_con_telefone2.getText());
            ListaCon.add(C);
        } else if (modo.equals("editar")) {
            int index = tbl_con_cons.getSelectedRow();
            ListaCon.get(index).setIdcontato(index);
            ListaCon.get(index).setNomeContato(c_con_nome.getText());
            ListaCon.get(index).setNomeContato(c_con_sobrenome.getText());
            ListaCon.get(index).setNomeContato(c_con_telefone1.getText());
            ListaCon.get(index).setNomeContato(c_con_telefone2.getText());
        }

        LoadTableCon();

        modo = "navegar";
        Interface();
        c_con_idcontato.setText("");
        c_con_nome.setText("");
        c_con_sobrenome.setText("");
        c_con_telefone1.setText("");
        c_con_telefone2.setText("");
    }//GEN-LAST:event_btn_con_salvarActionPerformed

